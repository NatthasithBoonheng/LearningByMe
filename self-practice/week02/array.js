function removeDuplicates(arr){
    for(i of arr){
        counter = 0
        for(let j = 0; j <= arr.length; j++){
            if(i == arr[j]){
                counter += 1
                if(counter > 1){
                    arr.splice(j,1)
                }
            }
        }
    }
    return arr
}

