// ‡∏Ñ‡∏∑‡∏ô‡∏Ñ‡πà‡∏≤‡∏≠‡∏≤‡∏£‡πå‡πÄ‡∏£‡∏¢‡πå‡∏ó‡∏µ‡πà‡∏™‡∏°‡∏≤‡∏ä‡∏¥‡∏Å‡∏ñ‡∏π‡∏Å‡πÄ‡∏£‡∏µ‡∏¢‡∏á‡∏•‡∏≥‡∏î‡∏±‡∏ö‡∏à‡∏≤‡∏Å‡∏´‡∏•‡∏±‡∏á‡∏°‡∏≤‡∏´‡∏ô‡πâ‡∏≤

function reverseArray(arr){
    newArr = []
    n = (arr.length - 1)
    while(n >= 0){
        newArr.push(arr[n])
        n -= 1
    }
    return newArr
}
